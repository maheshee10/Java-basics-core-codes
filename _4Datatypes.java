public class _4Datatypes
{
    public static void main(String args[]){
        //int data type
        int a = 10;
        byte b = 4;
        short s = 23;
        long l = 123L;
        //decimals data type
        float f = 3.5f;
        double d= 3.5;
        char c = 'k';
        boolean bool = true;
        boolean bool1 = false;
        System.out.println(a+""+b+""+s+""+l+""+f+""+d+""+c+""+bool+""+bool1);

    }
}
